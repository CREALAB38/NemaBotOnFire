void TOldPythonBased::setupMuscles()
{   //TNeuronArray muscles   ({iMVU, &MVL, &MDL, &MVR, &MDR});
	musDleft.AddNeuronen({&MDL07, &MDL08, &MDL09, &MDL10, &MDL11, &MDL12, &MDL13,
			&MDL14, &MDL15, &MDL16, &MDL17, &MDL18, &MDL19, &MDL20, &MDL21, &MDL22, &MDL23});
	musVleft.AddNeuronen({&MVL07, &MVL08, &MVL09, &MVL10, &MVL11, &MVL12, &MVL13,
			 &MVL14, &MVL15, &MVL16, &MVL17, &MVL18, &MVL19, &MVL20, &MVL21, &MVL22, &MVL23});
	musDright.AddNeuronen({&MDR07, &MDR08, &MDR09, &MDR10, &MDR11, &MDR12, &MDR13,
			 &MDR14, &MDR15, &MDR16, &MDR17, &MDR18, &MDR19, &MDR20, &MDL21, &MDR22, &MDR23});
	musVright.AddNeuronen( {&MVR07, &MVR08, &MVR09, &MVR10, &MVR11, &MVR12, &MVR13,
			&MVR14, &MVR15, &MVR16, &MVR17, &MVR18, &MVR19, &MVR20, &MVL21, &MVR22, &MVR23});
	// Muscle arrays are used to remove from Axon firing since muscles cannot fire.
	// Add the arrays using operator+
	musLeft.AddNeuronen( musDleft  + musVleft );  // was called mLeft
	musRight.AddNeuronen( musDright + musVright); // was called mRight
	musAll.AddNeuronen( musLeft   + musRight);  // was called muscleList
}

